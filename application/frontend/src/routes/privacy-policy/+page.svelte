  <h3>Introduction</h3>
  <p>
    Shadowself (We) is committed to protecting and respecting the privacy of our users ("You"). This Privacy Policy explains how we
    collect, use, process, disclose, and safeguard your information when you visit our website <a href="https://shadowself.io">
      shadowself.io</a> (the "Website") and use our services.
  </p>

  <h3>Information We Collect</h3>
  <p>
    We adhere to the principle of data minimization, meaning we only collect personal data that is essential for the specified
    purposes.
  </p>
  <ul>
    <li>
      <b>Account Credentials:</b> When you create an account, we collect a username and a password. Your password is stored in a cryptographically
      hashed format (meaning it's not human-readable and we cannot see your actual password).
    </li>
    <li>
      <b>Payment Information:</b> If you choose to make payments for our services using fiat money, these are processed by a secure third-party
      payment processor (e.g., Stripe). We do not directly collect, store, or process your full credit card numbers or bank account details.
      The payment processor may provide us with limited information such as a transaction ID, the last four digits of your card, and your
      billing country for record-keeping and service provision.
    </li>
    <li>
      <b>Contact Information:</b> If you choose to contact us via our <a href="/contact">contact page</a> or directly, you may provide your
      email address and any other information you include in your message. We collect this solely for the purpose of responding to your
      inquiries and providing support.
    </li>
  </ul>

  <h3>How We Use Your Information</h3>
  <p>
    We process your personal data only for specified, explicit, and legitimate purposes. The lawful bases for our processing under GDPR
    are as follows:
  </p>
  <ul>
    <li>
      <b>To Provide and Manage Your Account and Our Services:</b>
      <ul>
        <li>Creating and maintaining your user account.</li>
        <li>Authenticating you when you log in.</li>
        <li>Delivering the services you have requested.</li>
        <li>Processing your payments through our third-party payment processor.</li>
      </ul>
    </li>
    <li>
      <b>To Communicate With You:</b>
      <ul>
        <li>Responding to your inquiries, comments, or support requests submitted via our contact page or email.</li>
        <li>Sending you essential service-related updates. You cannot opt-out of essential service communications.</li>
      </ul>
    </li>
    <li>
      <b>To Ensure Security and Maintain Our Website:</b>
      <ul>
        <li>Monitoring for and preventing fraudulent or malicious activity.</li>
        <li>Troubleshooting and improving the performance and security of our Website.</li>
      </ul>
    </li>
    <li>
      <b>To Comply with Legal Obligations:</b>
      <ul>
        <li>
          We may process your data if required by law, such as in response to a court order, subpoena, or other legal process from a
          competent authority.
        </li>
      </ul>
    </li>
  </ul>

  <h3>Absence of Spyware and Analytics</h3>
  <p>
    We prioritize user privacy by refraining from the use of any spyware or analytics tools for behavioral tracking on the Website. We
    collect only the explicitly mentioned data and refrain from tracking user activity beyond what is strictly necessary for the core
    functionality of our services (e.g., session management for logged-in users).
  </p>

  <h3>Data Sharing and Disclosure</h3>
  <p>
    We do not sell, rent, or trade your personal information. We only share your data with third parties in the following limited
    circumstances:
  </p>
  <ul>
    <li>
      <b>Third-Party Payment Processors:</b> To facilitate payments, we share necessary information with secure third-party payment
      processors like Stripe. They are responsible for securely processing your payment details. We encourage you to review their
      privacy policies (<a href="https://stripe.com/privacy" target="_blank" rel="noopener noreferrer">Stripe's Privacy Policy</a>).
    </li>
    <li>
      <b>Essential Third-Party Service Providers:</b> We may use other third-party service providers who perform functions on our behalf,
      such as Twilio. These providers only have access to the personal information needed to perform their functions and are contractually
      obligated to protect your data and use it only for the purposes for which it was disclosed and in accordance with GDPR.
    </li>
    <li>
      <b>Legal Requirements:</b> We may disclose your information if required to do so by law or in the good faith belief that such action
      is necessary to: (a) comply with a legal obligation; (b) protect and defend our rights or property; (c) prevent or investigate possible
      wrongdoing in connection with the Service; (d) protect the personal safety of users of the Service or the public; or (e) protect against
      legal liability.
    </li>
  </ul>

  <h3>International Data Transfers</h3>
  <p>
    Your information, including personal data, may be transferred to — and maintained on — computers located outside of your state,
    province, country, or other governmental jurisdiction where the data protection laws may differ from those in your jurisdiction. If
    you are located in the European Economic Area (EEA), please note that some of our third-party service providers (like Stripe) may
    be based outside the EEA.
  </p>
  <p>
    When we transfer your personal data out of the EEA, we ensure a similar degree of protection is afforded to it by ensuring at least
    one of the following safeguards is implemented:
  </p>
  <ul>
    <li>
      The country to which the data is transferred has been deemed to provide an adequate level of protection for personal data by the
      European Commission (an "adequacy decision").
    </li>
    <li>
      We use specific contracts approved by the European Commission which give personal data the same protection it has in Europe
      (e.g., Standard Contractual Clauses - SCCs).
    </li>
  </ul>
  <p>
    Please contact us if you want further information on the specific mechanism used by us when transferring your personal data out of
    the EEA.
  </p>

  <h3>Data Security</h3>
  <p>
    We implement appropriate technical and organizational security measures designed to protect the security of any personal
    information we process. This includes measures like using HTTPS for data transmission, hashing passwords, and restricting access to
    personal data. However, it is crucial to acknowledge that no method of transmission over the Internet, or method of electronic
    storage, is 100% secure. While we strive to use commercially acceptable means to protect your personal information, we cannot
    guarantee its absolute security.
  </p>

  <h3>Data Retention</h3>
  <p>
    We will retain your personal data only for as long as is necessary for the purposes set out in this Privacy Policy. We will retain
    and use your personal data to the extent necessary to comply with our legal obligations (for example, if we are required to retain
    your data to comply with applicable laws, like financial record-keeping), resolve disputes, and enforce our legal agreements and
    policies.
  </p>
  <ul>
    <li>
      <b>Account Information:</b> Retained for as long as your account is active and for a reasonable period thereafter (for re-activation
      or audit purposes), or as necessary for our legitimate business purposes or legal compliance.
    </li>
    <li>
      <b>Contact Information (from inquiries):</b> Retained for as long as necessary to resolve your inquiry and for a short period thereafter
      for follow-up or record-keeping.
    </li>
  </ul>
  <p>
    Upon expiry of the applicable retention period, your personal data will be securely deleted or anonymized, unless a longer
    retention period is required or permitted by law.
  </p>

  <h3>Your Data Protection Rights under GDPR</h3>
  <p>
    If you are a resident of the European Economic Area (EEA), you have certain data protection rights. We aim to take reasonable steps
    to allow you to correct, amend, delete, or limit the use of your Personal Data.
  </p>
  <ul>
    <li>
      <b>Right to Access:</b> You have the right to request copies of your personal data that we hold.
    </li>
    <li>
      <b>Right to Rectification:</b> You have the right to request that we correct any information you believe is inaccurate or complete
      information you believe is incomplete.
    </li>
    <li>
      <b>Right to Erasure (Right to be Forgotten):</b> You have the right to request that we erase your personal data, under certain conditions
      (e.g., if the data is no longer necessary for the purpose it was collected).
    </li>
    <li>
      <b>Right to Restrict Processing:</b> You have the right to request that we restrict the processing of your personal data, under certain
      conditions.
    </li>
    <li>
      <b>Right to Object to Processing:</b> You have the right to object to our processing of your personal data based on our legitimate
      interests, under certain conditions.
    </li>
    <li>
      <b>Right to Data Portability:</b> You have the right to request that we transfer the data that we have collected to another organization,
      or directly to you, in a structured, commonly used, and machine-readable format, under certain conditions.
    </li>
    <li>
      <b>Right to Withdraw Consent:</b> If we are processing your personal data based on your consent, you have the right to withdraw that
      consent at any time. Withdrawal will not affect the lawfulness of processing before the withdrawal.
    </li>
  </ul>
  <p>
    Please note that we may ask you to verify your identity before responding to such requests. There may be legal reasons why we
    cannot fulfill your request, or can only do so in a limited way.
  </p>

